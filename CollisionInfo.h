#pragma once

#include <DirectXMath.h>
#include "Math/Vector3.h"

class Object3d;
class BaseCollider;

/// <summary>
/// 衝突情報
/// </summary>
class CollisionInfo
{
public:
	//判定情報
	CollisionInfo(Object3d* object, BaseCollider* collider, const Vector3& inter) {
		this->object = object;
		this->collider = collider;
		this->inter = inter;
	}

	//衝突相手のオブジェクト
	Object3d* object = nullptr;
	//衝突相手のコライダー
	BaseCollider* collider = nullptr;
	//衝突点
	Vector3 inter;
};

